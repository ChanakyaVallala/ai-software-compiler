const fs = require("fs");
const path = require("path");
const { buildGeneration } = require("../src/compiler");

const rootDir = path.resolve(__dirname, "..");
const promptsPath = path.join(__dirname, "prompts.json");
const reportPath = path.join(rootDir, "docs", "benchmark-report.md");
const latestResultPath = path.join(__dirname, "latest-result.json");

function runCase(prompt, category, index) {
  const started = Date.now();
  const result = buildGeneration({
    prompt,
    strictness: "strict",
    mode: "web-app",
    confidenceThreshold: 0.7
  });
  const latencyMs = Date.now() - started;

  return {
    id: `${category}_${index + 1}`,
    category,
    prompt,
    status: result.status,
    latencyMs,
    issueCount: result.issues.length,
    repairCount: result.repairSummary.total,
    runtimeStatus: result.runtimeReport.status,
    clarificationCount: result.clarifyingQuestions.length,
    success: result.status === "ready"
  };
}

function summarize(rows) {
  const total = rows.length;
  const successes = rows.filter(row => row.success).length;
  const failures = total - successes;
  const repairs = rows.reduce((sum, row) => sum + row.repairCount, 0);
  const latency = rows.reduce((sum, row) => sum + row.latencyMs, 0);
  const runtimeFailures = rows.filter(row => row.runtimeStatus === "fail").length;
  return {
    total,
    successes,
    failures,
    successRate: total ? Number((successes / total).toFixed(2)) : 0,
    repairs,
    avgLatencyMs: total ? Math.round(latency / total) : 0,
    runtimeFailures
  };
}

function renderReport(rows, summary) {
  const table = rows.map(row => `| ${row.id} | ${row.category} | ${row.status} | ${row.runtimeStatus} | ${row.issueCount} | ${row.repairCount} | ${row.clarificationCount} | ${row.latencyMs} |`).join("\n");
  return `# Benchmark Report

Generated by \`npm run benchmark\`.

## Summary

- Total prompts: ${summary.total}
- Successes: ${summary.successes}
- Failures: ${summary.failures}
- Success rate: ${summary.successRate}
- Repairs: ${summary.repairs}
- Average latency: ${summary.avgLatencyMs} ms
- Runtime failures: ${summary.runtimeFailures}

## Results

| ID | Category | Status | Runtime | Issues | Repairs | Clarifications | Latency ms |
| --- | --- | --- | --- | ---: | ---: | ---: | ---: |
${table}
`;
}

function main() {
  const prompts = JSON.parse(fs.readFileSync(promptsPath, "utf8"));
  const rows = [
    ...prompts.realWorld.map((prompt, index) => runCase(prompt, "realWorld", index)),
    ...prompts.edgeCases.map((prompt, index) => runCase(prompt, "edgeCase", index))
  ];
  const summary = summarize(rows);
  fs.mkdirSync(path.dirname(reportPath), { recursive: true });
  fs.writeFileSync(reportPath, renderReport(rows, summary));
  fs.writeFileSync(latestResultPath, JSON.stringify({ summary, rows, generatedAt: new Date().toISOString() }, null, 2));
  console.log(JSON.stringify({ summary, reportPath }, null, 2));
}

main();
